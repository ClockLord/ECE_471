static int max = 65535;
//function sets PWM to a desired value from 0 - 11
void setPWM(enum led led, int value){

	int dutyCycle = 0;


		switch(value){	//set the duty cycle
			case 0:
				dutyCycle =0;
				break;
			case 1:
				dutyCycle =(max/10);
				break;
			case 2:
				dutyCycle =2*(max/10);
				break;
			case 3:
				dutyCycle =3*(max/10);
				break;
			case 4:
				dutyCycle =4*(max/10);
				break;
			case 5:
				dutyCycle =5*(max/10);
				break;
			case 6:
				dutyCycle =6*(max/10);
				break;
			case 7:
				dutyCycle =7*(max/10);
				break;
			case 8:
				dutyCycle =8*(max/10);
				break;
			case 9:
				dutyCycle =9*(max/10);
				break;
			case 10:
				dutyCycle = max;
				break;
			default:
				dutyCycle = 0;
		}


	if(led == green){
        TIM3->CCR3 = dutyCycle;
	}
	else if (led == red){
        TIM12->CCR1 = dutyCycle;
	}
}

