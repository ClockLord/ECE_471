//function sets PWM to a desired value from 0 - 11
void setPWM(enum led led, int value){

	int dutyCycle = 0;

		switch(value){	//set the duty cycle
			case 1:
				dutyCycle =(max/10);
				break;
			case 2:
				dutyCycle =2*(max/10);
				break;
			case 3:
				dutyCycle =3*(max/10);
				break;
			case 4:
				dutyCycle =4*(max/10);
				break;
			case 5:
				dutyCycle =5*(max/10);
				break;
			case 6:
				dutyCycle =6*(max/10);
				break;
			case 7:
				dutyCycle =7*(max/10);
				break;
			default:
				dutyCycle =3*(max/10);
		}
		//make sure duty cycle stays within the range
		if(value>7){
			dutyCycle =7*(max/10);
		}
		else if(value<1){
			dutyCycle =(max/10);
		}

		if(led == green){
			TIM3->CCR3 = dutyCycle;
		}

		else if (led == red){
			TIM12->CCR1 = dutyCycle;
		}
}
